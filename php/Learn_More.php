                <h3>Blood is a Gift</h3>
                <p>The gift of blood helps save lives, and the demand for blood and blood products is great. Every 2 seconds, someone in the U.S. needs blood, and more than 50,000 units of blood are needed each year at Cedars-Sinai alone. Learn more about blood types and compatibility, different ways to donate and the need for lifesaving blood so you can make an informed decision about donation. It's important to know your blood type—whether you're a donor or a patient in need of blood products. You may be surprised to learn how many people will benefit from your generous gift, no matter what type you have.</p>
                <h3>Compatibility</h3>
                <p>Everyone who is eligible can donate blood, but depending on your blood type, you're only able to receive certain types of blood, plasma or platelets.</p>
                <h3>Whole Blood</h3>
                <p>There are eight different blood types, and they're not always compatible with each other. Interestingly, people who have O-negative blood are considered universal donors for the entire population. Similarly, AB-positive is the universal acceptor blood group and people with this blood group are called universal receptors. Because AB-positive individuals have all of the possible antigens, their immune system is used to seeing each of the antigens and , hence, won't attack any of the antigens!</p>
                <p>There are no substitutes for blood, platelets or plasma—they cannot be manufactured. Patients in need of blood or blood products must rely on donations from people like you. Those who are hesitant about donating blood for the first time often find that the donation process is easy and that saving lives is deeply gratifying.</p>
                <h3>Facts</h3>
                <p>One donation can save multiple lives. Here are the facts:</p>
                <ul>
                    <li>Just 1 donation can save up to 3 lives.</li>
                    <li>The average red blood cell transfusion is 3 pints (or 3 whole-blood donations).</li>
                    <li>More than 1 million people every year are diagnosed with cancer for the first time. Many of them will need blood—sometimes daily—during chemotherapy.</li>
                    <li>More than 38,000 blood donations are needed every day.</li>
                    <li>Type O-negative whole blood can be transfused to people with any blood type, but this type of blood is rare, and supplies of it are low.</li>
                    <li>Type AB plasma can be transfused to patients with all other blood types, but it's also in short supply.</li>
                    <li>While 38% of the World population is eligible to give blood, only 2% actually donates.</li>
                </ul>
